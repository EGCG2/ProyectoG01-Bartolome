package projectOperation_egc;

public class operation {
	
	 public static int suma(int numero1, int numero2){
		    
		    int resultado = 0;
		    
		    //Sumamos los dos numeros
		    resultado = numero1 + numero2;
		    
		    //Retornamos el valor
		    return resultado;
	}
	 
	 public static int resta(int numero1, int numero2){
		    
		    int res = 0;
		    
		    //Sumamos los dos numeros
		    res = numero1 - numero2;
		    
		    //Retornamos el valor
		    return res;
	}
	
	
}
